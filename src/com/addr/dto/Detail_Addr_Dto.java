public class Detail_Addr_Dto {
	private int detail_no;
	private String detail_addr;
	private String detail_addr_number;
	
	public int getDetail_no() {
		return detail_no;
	}
	public void setDetail_no(int detail_no) {
		this.detail_no = detail_no;
	}
	public String getDetail_addr() {
		return detail_addr;
	}
	public void setDetail_addr(String detail_addr) {
		this.detail_addr = detail_addr;
	}
	public String getDetail_addr_number() {
		return detail_addr_number;
	}
	public void setDetail_addr_number(String detail_addr_number) {
		this.detail_addr_number = detail_addr_number;
	}
	
}
