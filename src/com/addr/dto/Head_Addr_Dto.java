package com.addr.dto;

public class Head_Addr_Dto {
	private int head_no;
	private String head_addr;
	private String head_addr_number;
	
	public int getHead_no() {
		return head_no;
	}
	public void setHead_no(int head_no) {
		this.head_no = head_no;
	}
	public String getHead_addr() {
		return head_addr;
	}
	public void setHead_addr(String head_addr) {
		this.head_addr = head_addr;
	}
	public String getHead_addr_number() {
		return head_addr_number;
	}
	public void setHead_addr_number(String head_addr_number) {
		this.head_addr_number = head_addr_number;
	}
	
}
